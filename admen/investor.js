

//Function to create new account.

function CreateAccount()
{
    var logIn=document.getElementById("log_in");
    logIn.style.display="none";
    var newAccount=document.getElementById("account");
    newAccount.style.display="block";
}
//Function to retrieve an account.

function RetreiveAccount()
{
    var logIn=document.getElementById("log_in");
    logIn.style.display="none";
    var RetreiveAccount=document.getElementById("Retrieve_account");
    RetreiveAccount.style.display="block";
    
}
